  <p>私自身が音楽を本格的に勉強し始めて分かった事は、その奥深さです。果てしない可能性があります。数百年に渡って研究され、体系化されてきた今でも、まだ進化しています。私自身学ぶほどに湧き出る自分の音楽に、喜び、また驚きました。音楽を通じての感動は、今日、この瞬間にも尽きることはありません。音楽は国境、人種、年齢、プロアマ問わず、人と人を、心と心を結びつける素晴らしい芸術です。そして音楽に携わる事の誇り高き人生。毎日の音楽の中に、新しい自分を発見し、この音楽が、大切な人たちとの出会いに繋がっています。
特殊な経歴を経て、プロフェッショナルとして活動の場を頂いてきました。この大好きな音楽、掛け替えのない音楽、多くの先人達が遺してくれた軌跡、私自身が先生達から教わった技術と音楽の心を、ひとりでも多くの人に伝えていきたいとの思いから、講師、研究者としてのキャリアをスタートさせました。有難い事に、音楽教室、専門学校、大学等から講義に招いて頂き、多角的な視点から音楽を見る機会を得ています。</p>

  <p>私が「講師」というフィールドに乗り出した理由は、アメリカ留学時の先生方からの教えにあります。
「音楽家は、常に、演奏出来なければならない。新しい表現が出来なければならない。そして誰かの疑問に、全て答えなければならない。それがプロフェッショナルというもの」ということです。
この考え方は、職業として音楽家を志して以来、私自身が追求する生涯の道標となっています。演奏家、作曲家であると同時に、研究者であり、伝承者であり、そして講師なのです。
講師は生徒にすばらしい音楽を伝えることが仕事であり、また音楽の素養を、惜しむことなく生徒に伝えるということが、プロフェッショナルとして、最大の喜びだと確信しています。作曲家、演奏家を問わず、ひとりでも多くの音楽家に成長していって頂ける事が、私自身の存在意義でもあります。
</p>



