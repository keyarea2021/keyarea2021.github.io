  <p>物心付けば家にはピアノがあってギターがあって、サッチモのアルバムがあって・・・
「自分は何の楽器をやろうかな？」と思い、手にしたのがベース・ギター。12歳から始めてすぐに、洋楽－特にブラック・コンテンポラリーとAORにどっぷり浸かり、15歳の時学園祭ライブ・デビュー。高校時代は競泳をやりながら春と秋はライブハウスや学園祭でバンド活動。寝ても覚めても演奏していました。
大学卒業後、ラジオ局の仕事を通じて多くの素晴らしいミュージシャンに出会いました。マーカス・ミラー、ラリー・グラハム、T.M.スティーブンス、アルフォンソ・ジョンソン、ナイル・ロジャース、そして少年時代から憧れていた、クインシー・ジョーンズ。雲の上の存在だった彼らに教わった事は、「音楽に隔たりはない」ということです。彼らの後押しを受けて、音楽に生きる道を選択し、勉強と研鑽の旅－アメリカ留学を決意しました。27歳の時でした。</p>

  <p>ジャズをきっかけとして音楽を勉強する過程で、今まで経験してこなかった他の音楽に関心を寄せていきました。音楽の根底にある本質は共通しているのです。そして今、あらゆるジャンルを演奏してきた事で「自分」を作り上げたのだと思います。
演奏をし続けるからこそ見えてくる「新しいもの」があります。楽器を手にしてからずいぶん経ちますが、いまだに飽きる事がありません。きっと毎日が発見に溢れていて、音楽に惹かれ続けているからだと感じています。
ベースという楽器。例えるなら、家族にとっての「家」。一緒にアンサンブルを奏でる仲間達が安心して帰ってくる場所。みんなを送り出す場所。振り返れば、必ず支えてくれる堂々とした存在。とでも言えましょう。だからこそ、「リズムの要」「ハーモニーの土台」そして、「メロディの一部」なのです。</p>


