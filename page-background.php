  <p>留学時代に演奏技術を極めるつもりで始めた勉強は、自分の中にもう一つ、大きな命題を与えてくれました。それは「作曲」です。より良いベース・ラインを演奏するための努力は、作曲と編曲への強い関心も高めていったのです。演奏活動と創作活動は、あらゆるミュージシャンにとっての両輪を担うものです。</p>

  <p>人は経験から新しいものを作ります。いい音楽をたくさん聴いて、見て、感動して、自分自身の中で熟成する。そのことが、やがて人の心を動かす自分の音楽を生み出す力になります。
例えばポップスは、「その時代のクリエイティブの集大成」です。過去の作品を超えたいというクリエイティブの気持ちと、その時代のリスナーの気持ちの掛け算です。夢と希望と、自信とプライドを持って、そして音楽への愛をたっぷり注いで。私たちが作る次の曲も多くの人たちに愛されていく可能性があるのです。

ジャズはひと言で表すなら、「クリエイティブの最先端」。夢のある音楽です。表現の可能性をどんどん追求できるフィールドです。毎日いろいろなモノを聴いて、演奏して、表現する。
そして新しいハーモニー、アンサンブル、メロディの可能性を切り拓いていく。音楽の最先端にいることの大切さを感じさせてくれます。</p>

  <p>マスメディアの営業マン、企画マンとしても過ごした経験から、数多くのコマーシャル用ＢＧＭを制作する機会を頂いてきました。テーマ曲を作る仕事には、歌を作る事とは別の「独特なうねり」が存在します。
CMの主役はあくまで商品やブランド、映像や言葉ですが、背景を彩る音楽は送り手と受け手を繋ぐために、欠かすことのできない存在なのです。文脈にあるイメージをメロディにする事は、作曲家にとって大変なチャレンジです。なおかつ、限られた秒数、タイミング、ダイナミクス、バランスを考慮したプロダクションを完成させるためには、音楽のあらゆる要素に耳を傾けている必要があるのです。また、コマーシャル制作の一端を担う事は、様々な業種、職種の人達と精力的に交流を深めながら、自分を高めていく素晴らしい機会です。こうした経験や感動が、さらなる表現力を高めることにも繋がっているのです。
作曲家の仕事を通じて、人として成長したい。音楽とは本来、そのような仕事であるはずです。</p>

